#include <iostream>
using namespace std;
int main()
{
   double var1 = 2;
   double var2 = 4;
   cout<<"Variable one is: "<<var1<<endl;
   cout<<"Variable two is: "<<var2<<endl;
   cout<<"var1 + var2 = "<<var1 + var2<<endl;
   cout<<"var1 - var2 = "<<var1 - var2<<endl;
   cout<<"var1 * var2 = "<<var1 * var2<<endl;
   cout<<"var1 / var2 = "<<var1 / var2<<endl;
}
